/// Classe contenente le costanti utili per la serializzazione e
/// deserializzazione delle risorse offline.
class CostantiOffline {
  /// Stringa utile per ricavare l'url univoco della risorsa.
  static const idUrl = 'Id Url';

  /// Stringa utile per ricavare l'indice univoco della risorsa.
  static const idIndice = 'Id Indice';

  /// Stringa utile per ricavare la lista degli elementi della risorsa.
  static const listaElementi = 'Lista Elementi';

  /// Stringa utile per ricavare il path dell'immagine locale della risorsa.
  static const pathImmagine = 'Path Immagine';
}
